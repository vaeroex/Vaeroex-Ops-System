const assert = require("node:assert/strict");
const fs = require("node:fs");
const path = require("node:path");
const Module = require("node:module");
const ts = require("typescript");

const root = process.cwd();

require.extensions[".ts"] = function compileTypeScript(module, filename) {
  const source = fs.readFileSync(filename, "utf8");
  const output = ts.transpileModule(source, {
    compilerOptions: { esModuleInterop: true, module: ts.ModuleKind.CommonJS, moduleResolution: ts.ModuleResolutionKind.NodeJs, target: ts.ScriptTarget.ES2022 },
    fileName: filename
  });
  module._compile(output.outputText, filename);
};

const originalResolveFilename = Module._resolveFilename;
Module._resolveFilename = function resolveAlias(request, parent, isMain, options) {
  if (request.startsWith("@/")) return originalResolveFilename.call(this, path.join(root, request.slice(2)), parent, isMain, options);
  return originalResolveFilename.call(this, request, parent, isMain, options);
};

const { buildBusinessIntelligenceCoverage } = require("../lib/intelligence/coverage.ts");
const { buildIntelligenceLayer } = require("../lib/intelligence/layer.ts");

const now = "2026-07-12T12:00:00.000Z";
const file = (overrides = {}) => ({
  id: "file-1", display_name: "Monthly revenue report", original_name: "revenue.csv", file_extension: "csv", import_type: "kpi", imported_rows: 10,
  processing_status: "ready", created_at: now, updated_at: now, processed_at: now, archived_at: null, deleted_at: null, metadata_json: {}, ...overrides
});
const chunk = (index, overrides = {}) => ({
  id: `chunk-${index}`, source_type: "file", source_id: "file-1", source_file_id: "file-1", source_title: "Monthly revenue report", source_excerpt: "Revenue evidence", chunk_index: index,
  created_at: now, updated_at: now, indexed_at: now, archived_at: null, deleted_at: null, source_metadata: {}, ...overrides
});
const setupSignal = () => ({
  id: "setup-signal", title: "Customize starter forms", category: "Setup", description: "Review generated forms and adjust required fields.", status: "Business Signal", created_at: now, updated_at: now
});
const generatedReport = () => ({
  id: "generated-report", title: "Weekly Intelligence Report - Generated by Vaeroex", report_type: "Weekly Intelligence Report", body_markdown: "Generated by Vaeroex", source_data_json: { generatedFrom: "setup" }, created_at: now, updated_at: now, date_range_end: "2026-07-12", archived_at: null, deleted_at: null
});
const completedRun = () => ({ id: "run-1", agent_type: "ask_vaeroex", status: "completed", input_json: {}, output_json: {}, created_at: now, updated_at: now, archived_at: null, deleted_at: null });

const onlyDerived = buildBusinessIntelligenceCoverage({ tasks: [setupSignal()], reports: [generatedReport()], vaeroexRuns: [completedRun()] });
assert.equal(onlyDerived.evidenceSummary.originalEvidenceCount, 0, "setup rows, generated reports, and completed runs are not original evidence");
assert.equal(onlyDerived.sourceMix.some((item) => /vaeroex memory|run/i.test(item.label)), false, "run history is never presented as a source mix");

const oneFileManyChunks = buildBusinessIntelligenceCoverage({ files: [file()], memoryChunks: Array.from({ length: 12 }, (_, index) => chunk(index)) });
assert.equal(oneFileManyChunks.evidenceSummary.originalEvidenceCount, 1, "many chunks from one file count as one original source");
assert.equal(oneFileManyChunks.evidenceSummary.memoryItemCount, 12, "memory item count remains separate from original evidence");
assert.equal(oneFileManyChunks.categories.find((item) => item.id === "revenue")?.sourceCount, 1, "coverage uses the original file once");

const inactiveSignalChunk = buildBusinessIntelligenceCoverage({
  tasks: [setupSignal()],
  memoryChunks: [chunk(1, { source_type: "business_signal", source_id: "setup-signal", source_file_id: null })]
});
assert.equal(inactiveSignalChunk.evidenceSummary.memoryItemCount, 0, "chunks linked to inactive setup signals are excluded before counting");

const archivedFile = buildBusinessIntelligenceCoverage({ files: [file({ archived_at: now })], memoryChunks: [chunk(1)] });
assert.equal(archivedFile.evidenceSummary.originalEvidenceCount, 0, "archived evidence is excluded");
assert.equal(archivedFile.evidenceSummary.memoryItemCount, 0, "chunks from archived files are excluded");

const insufficient = buildIntelligenceLayer({ tasks: [setupSignal()], reports: [generatedReport()], vaeroexRuns: [completedRun()] });
assert.equal(insufficient.businessHealth.available, false, "derived or setup-only data cannot produce a precise Business Health score");
assert.equal(insufficient.businessHealth.status, "Insufficient Data", "insufficient evidence is explicit rather than negative performance");

process.stdout.write("Evidence lineage regressions passed.\n");
