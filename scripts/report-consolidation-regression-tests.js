const fs = require("node:fs");
const assert = require("node:assert/strict");

const read = (path) => fs.readFileSync(path, "utf8");
const shell = read("components/app/AppShell.tsx");
const reports = read("app/app/reports/page.tsx");
const action = read("app/app/reports/actions.ts");
const presentation = read("lib/reports/presentation.ts");
const scheduled = read("lib/reports/scheduled-generator.ts");
const briefings = read("app/app/briefings/page.tsx");
const outputs = read("app/app/actions/page.tsx");
const generatedDraft = read("app/app/reports/new/page.tsx");
const legacyGeneratedDraft = read("app/app/generated/new/page.tsx");
const generatedAction = read("app/app/generated/actions.ts");
const reportDetail = read("app/app/reports/[id]/page.tsx");

assert.match(shell, /href: "\/app\/reports", label: "Reports"/);
assert.doesNotMatch(shell, /label: "Briefings"|label: "Generated Outputs"/);
assert.match(briefings, /permanentRedirect\("\/app\/reports"\)/);
assert.match(outputs, /permanentRedirect\("\/app\/reports"\)/);
for (const label of ["Executive Brief", "Board Report", "Improvement Plan", "Investigation Summary"]) assert.match(`${reports}\n${presentation}`, new RegExp(label));
assert.match(reports, /View report/);
assert.match(reports, /ReportLifecycleMenu/);
assert.doesNotMatch(action, /from\("ai_agent_runs"\)/);
assert.doesNotMatch(scheduled, /from\("ai_agent_runs"\)/);
assert.doesNotMatch(generatedDraft, /from\("ai_agent_runs"\)|from\("reports"\)/);
assert.match(legacyGeneratedDraft, /permanentRedirect\(`\/app\/reports\/new/);
assert.match(generatedDraft, /Draft · Not saved/);
assert.match(generatedDraft, /Save report/);
assert.doesNotMatch(generatedDraft, /GeneratedOutputControls|Download Markdown|Print \/ Save PDF/);
assert.match(generatedAction, /source_record_ids/);
assert.match(generatedAction, /\.select\("id"\)\s*\.single\(\)/s);
assert.match(reportDetail, /Generated from Intelligence finding/);
assert.match(scheduled, /filterOriginalBusinessEvidence/);
assert.match(scheduled, /derived_analysis: true/);
assert.match(action, /derived_analysis: true/);
assert.match(action, /original_source_count/);
assert.doesNotMatch(action, /recordDailyBusinessHealthSnapshot/);
console.log("Report consolidation regression tests passed.");
